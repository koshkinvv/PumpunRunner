#!/bin/bash
# Простой скрипт для развертывания в Replit
# Основная задача - запустить веб-сервер на порту 5000

# Создаем директорию для логов
mkdir -p logs

# Записываем информацию о запуске
echo "$(date) - Запуск развертывания" > logs/deployment.log

# Запускаем веб-сервер
gunicorn --bind 0.0.0.0:5000 app:app